#include "3-calc.h"
/**
 * op_add - returns the sum
 * @a: first input
 * @b: second input
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_mul - returns the multiplication
 * @a: first input
 * @b: second input
 * Return: the multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_sub - returns the sub
 * @a: first input
 * @b: second input
 * Return: the sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_div - returns the div
 * @a: first input
 * @b: second input
 * Return: the div
 */
int op_div(int a, int b)
{
		return (a / b);
}
/**
 * op_mod - returns the modular
 * @a: first input
 * @b: second input
 * Return: the mod
 */
int op_mod(int a, int b)
{
		return (a % b);
}
